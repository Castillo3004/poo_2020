
package clase4_deber;

public class Calculo_terreno {
    private int altura;
    private int base; 
    private int lado; 
    private float costo_terr;

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getLado() {
        return lado;
    }

    public void setLado(int lado) {
        this.lado = lado;
    }

    public float getCosto_terr() {
        return costo_terr;
    }

    public void setCosto_terr(float costo_terr) {
        this.costo_terr = costo_terr;
    }
    
}
